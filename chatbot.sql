-- Tạo csdl
CREATE DATABASE chatbot;
USE chatbot;

-- Tạo bảng đơn vị (Department)
CREATE TABLE DonVi (
  MaDonVi TEXT PRIMARY KEY,
  TenDonVi TEXT NOT NULL
);

-- Tạo bảng chủ đề câu hỏi (Question Topic)
CREATE TABLE ChuDeCauHoi (
  MaChuDe TEXT PRIMARY KEY,
  TenChuDe TEXT
);

-- Tạo bảng câu trả lời (Answer Document)
CREATE TABLE VanBanTraLoi (
  MaVanBanId INTEGER PRIMARY KEY AUTOINCREMENT, 
  MaVanBan TEXT NOT NULL,
  CauTraLoi TEXT NOT NULL,
  MaDonVi TEXT NOT NULL,
  ThoiGianTraLoi DATETIME DEFAULT CURRENT_TIMESTAMP, 
  FOREIGN KEY (MaDonVi) REFERENCES DonVi(MaDonVi)
);

-- Tạo bảng câu hỏi (Question)
CREATE TABLE CauHoi (
  CauHoiId INTEGER PRIMARY KEY AUTOINCREMENT,
  MaCauHoi TEXT DEFAULT NULL,
  CauHoi TEXT NOT NULL,
  MaDonVi TEXT DEFAULT NULL,
  MaChuDe TEXT DEFAULT NULL,
  MaVanBan TEXT DEFAULT NULL,
  Email TEXT DEFAULT NULL,
  ThoiGianHoi DATETIME DEFAULT CURRENT_TIMESTAMP, 
  CoThayDoi INTEGER DEFAULT 0,          
  CoTraLoi INTEGER DEFAULT 0,           
  KhongTraLoi INTEGER DEFAULT 0,         
  NguoiHoiId INTEGER DEFAULT NULL,
  FOREIGN KEY (MaDonVi) REFERENCES DonVi(MaDonVi),
  FOREIGN KEY (MaChuDe) REFERENCES ChuDeCauHoi(MaChuDe)
);

-- Tạo bảng câu hỏi cho ML (Questions for ML)
CREATE TABLE CauHoiML (
  CauHoiMLId INTEGER PRIMARY KEY AUTOINCREMENT, 
  MaCauHoi TEXT DEFAULT NULL,
  CauHoi TEXT NOT NULL
);

